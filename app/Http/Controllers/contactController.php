     */
    public function store(Request $request)
    {
        //dd($request->all());
        $request->validate([
            'name'=>'required|max:25|min:3',
            'email'=>'required',
            'message'=>'required',
        ]);
        //insert database
          $obj=new contactModel();
          $obj->name=$request->name;
          $obj->email=$request->email;
          $obj->message=$request->message;
          $obj->save();


        return "Successfully submited!";
    }


